int	__isnbr(char *str)
{
	if ((*str == '-' || *str == '+' )&& ft_strlen(str) > 1)
		str++;
	while (*str)
	{
		if ((*str < 48 || *str > 57))
			return (0);
		str++;
	}
	return (1);
}